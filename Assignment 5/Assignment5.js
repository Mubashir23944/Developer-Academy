// Example of Equal (==)
var runs = 100;
if(runs == 100){
    console.log('Example of Equal to Equal to:','Your century is completed');
}

// Example of greater than (>)
runs = 101;

if(runs > 100){
    console.log('Example of Greater than:','You are not out even after century');
}

// Example of less than (<)
runs = 99;

if(runs < 100){
    console.log('Example of Less than:','You got unluckily out before your century');
}

